// Language detection and management
class LanguageManager {
    constructor() {
        this.currentLanguage = 'en';
        this.supportedLanguages = Object.keys(translations);
        this.init();
    }

    init() {
        // Try to get language from localStorage first
        const savedLanguage = localStorage.getItem('preferred-language');
        if (savedLanguage && this.supportedLanguages.includes(savedLanguage)) {
            this.currentLanguage = savedLanguage;
        } else {
            // Detect browser language
            const browserLang = navigator.language.slice(0, 2);
            if (this.supportedLanguages.includes(browserLang)) {
                this.currentLanguage = browserLang;
            }
        }
        
        this.createLanguageSelector();
        this.applyTranslations();
    }

    createLanguageSelector() {
        // Check if selector already exists
        if (document.querySelector('.language-selector')) {
            return;
        }

        const languageNames = {
            en: 'EN',
            es: 'ES',
            fr: 'FR',
            de: 'DE',
            zh: 'ZH',
            ja: 'JA',
            pt: 'PT',
            ru: 'RU',
            ar: 'AR'
        };

        const selector = document.createElement('div');
        selector.className = 'language-selector';
        
        const select = document.createElement('select');
        select.id = 'language-select';
        
        this.supportedLanguages.forEach(lang => {
            const option = document.createElement('option');
            option.value = lang;
            option.textContent = languageNames[lang];
            option.selected = lang === this.currentLanguage;
            select.appendChild(option);
        });
        
        select.addEventListener('change', (e) => {
            this.changeLanguage(e.target.value);
        });
        
        selector.appendChild(select);

        // Insert into navbar
        const navbar = document.querySelector('.navbar');
        if (navbar) {
            navbar.appendChild(selector);
            console.log('Language selector created and added to navbar');
        } else {
            console.error('Navbar not found');
        }
    }

    changeLanguage(newLanguage) {
        if (this.supportedLanguages.includes(newLanguage)) {
            this.currentLanguage = newLanguage;
            localStorage.setItem('preferred-language', newLanguage);
            this.applyTranslations();
            
            // Update document language attribute
            document.documentElement.lang = newLanguage;
            
            // Handle RTL languages
            if (newLanguage === 'ar') {
                document.documentElement.dir = 'rtl';
                document.body.classList.add('rtl');
            } else {
                document.documentElement.dir = 'ltr';
                document.body.classList.remove('rtl');
            }
        }
    }

    applyTranslations() {
        const elements = document.querySelectorAll('[data-translate]');
        elements.forEach(element => {
            const key = element.getAttribute('data-translate');
            const translation = translations[this.currentLanguage][key];
            if (translation) {
                element.textContent = translation;
            }
        });
    }

    t(key) {
        return translations[this.currentLanguage][key] || translations.en[key] || key;
    }
}

// Initialize when DOM is loaded
let languageManager;
document.addEventListener('DOMContentLoaded', () => {
    languageManager = new LanguageManager();
});
